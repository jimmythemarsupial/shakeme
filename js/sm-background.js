class SMBackground {
	constructor() {
		
	}
	
	restart() {
		logger.log('Restarted Shake Me application');
	}
	
	start() {
		logger.log('Started Shake Me application');
	}
}


/***************************************************************************************************
*
*
***************************************************************************************************/
var background = new SMBackground();


/***************************************************************************************************
*
*
***************************************************************************************************/
chrome.runtime.onInstalled.addListener(function() {
	background.start();
});


/***************************************************************************************************
*
*
***************************************************************************************************/
chrome.runtime.onStartup.addListener(function() {
	background.restart();
});